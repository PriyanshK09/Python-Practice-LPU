#Take Values of LENGTH and BREADTH from USER
#Print if its a SQUARE or NOT

print("Enter the SIDES of the Quadrilateral : ")
x = int(input("Enter length of given Quadrilateral : "))
y = int(input("Enter breadth of given Quadrilateral : "))

