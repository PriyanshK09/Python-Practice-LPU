#Take input from user for Two Numbers (x and y)
number1 = input("Enter 1st Number : ")
number2 = input("Enter 2nd Number : ")

print("Before SWAP, Value of 1st Number is ", number1)
print("Before SWAP, Value of 2nd Number is ", number2)

#swapping numbers
number3 = number1
number1 = number2
number2 = number3

print("After SWAP, Value of 1st Number is ", number1)
print("After SWAP, Value of 2nd Number is ", number2)
